const { Pool } = pg;

// Get connection string from environment
const connectionString = process.env.DATABASE_URL;

let pool: pg.Pool | null = null;

if (connectionString) {
  console.log("Initializing database pool with connection string...");
  pool = new Pool({
    connectionString,
    ssl: {
      rejectUnauthorized: false // Required for Supabase/hosted databases
    }
  });
} else {
  console.error("WARNING: DATABASE_URL env variable is not set!");
}

