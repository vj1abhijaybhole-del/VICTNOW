  // Razorpay Order Creation
  app.post("/api/razorpay/create-order", async (req, res) => {
    const { amount, currency } = req.body;
    if (!amount) {
      return res.status(400).json({ error: "Amount is required" });
    }
    const rzp = getRazorpay();
    const curr = currency || "INR";
    const amountInPaisa = Math.round(amount * 100);

    if (!rzp) {
      // Mock order for sandbox simulator
      const mockOrderId = "order_mock_" + Math.floor(100000 + Math.random() * 900000);
      return res.json({
        id: mockOrderId,
        amount: amountInPaisa,
        currency: curr,
        isMock: true
      });
    }

    try {
      const order = await rzp.orders.create({
        amount: amountInPaisa,
        currency: curr,
        receipt: "receipt_order_" + Date.now()
      });
      res.json(order);
    } catch (err: any) {
      console.error("Razorpay SDK order creation error:", err);
      res.status(500).json({ error: err.message || "Failed to create Razorpay order" });
    }
  });

  // Razorpay Signature Verification
  app.post("/api/razorpay/verify-payment", async (req, res) => {
    const { razorpay_order_id, razorpay_payment_id, razorpay_signature, isMock } = req.body;

    if (isMock || (razorpay_order_id && razorpay_order_id.startsWith("order_mock_"))) {
      return res.json({ success: true, verified: true, isMock: true });
    }

    const keySecret = process.env.RAZORPAY_KEY_SECRET;
    if (!keySecret) {
      return res.status(500).json({ error: "Razorpay secrets not configured on server" });
    }

    try {
      const text = razorpay_order_id + "|" + razorpay_payment_id;
      const generated_signature = crypto
        .createHmac("sha256", keySecret)
        .update(text)
        .digest("hex");

      if (generated_signature === razorpay_signature) {
        res.json({ success: true, verified: true });
      } else {
        res.status(400).json({ error: "Razorpay signature verification failed" });
      }
    } catch (err: any) {
      res.status(500).json({ error: err.message || "Failed to verify signature" });
    }
  });

  // Admin credentials store configuration
  const CREDENTIALS_FILE = path.join(process.cwd(), "admin-credentials-store.json");

  function getAdminCredentials() {
    try {
      if (fs.existsSync(CREDENTIALS_FILE)) {
        const data = JSON.parse(fs.readFileSync(CREDENTIALS_FILE, "utf-8"));
        if (data.username && data.password) {
          return data;
        }
      }
    } catch (err) {
      console.error("Error reading admin credentials file:", err);
    }
    const username = process.env.ADMIN_USERNAME || "admin";
    const password = process.env.ADMIN_PASSWORD || "adminpassword";
    try {
      fs.writeFileSync(CREDENTIALS_FILE, JSON.stringify({ username, password }, null, 2));
    } catch (err) {
      // Ignored
    }
    return { username, password };
  }

  const ADMIN_TOKEN = "Bearer admin-secret-session-token-9988";

  // Admin Authentication Middleware
  const requireAdminAuth = (req: express.Request, res: express.Response, next: express.NextFunction) => {
    const authHeader = req.headers.authorization;
    if (!authHeader || authHeader !== ADMIN_TOKEN) {
      return res.status(401).json({ error: "Unauthorized access to administrator APIs" });
    }
    next();
  };

